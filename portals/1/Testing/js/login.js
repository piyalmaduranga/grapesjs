var getURLParameter = function (url_, sParam) {
    var sPageURL = decodeURIComponent(url_.substring(1).replace(/\+/g, '%20')),
        sURLVariables = sPageURL.split('&'),
        sParameterName,
        i;

    for (i = 0; i < sURLVariables.length; i++) {
        sParameterName = sURLVariables[i].split('=');

        if (sParameterName[0] === sParam) {
            return sParameterName[1] === undefined ? true : sURLVariables[i].substr(sParam.length + 1 , sURLVariables[i].length);
        }
    }
};
